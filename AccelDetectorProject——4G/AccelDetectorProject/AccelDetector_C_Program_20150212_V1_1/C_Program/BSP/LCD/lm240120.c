/*---------------------------------------------------------------------------
**	Function	: 
**	Parameters	:
**	Retval		:
**	Description	: 使用PE的高8位作为数据总线
---------------------------------------------------------------------------*/
void LM_SdCmd(u8 Command)   //send command
{
	GPIO_WriteBit(LM_DC_GPIO_Port, LM_DC_GPIO_Pin, Bit_RESET);	
	GPIO_Write(LM_DATA_BUS_Port, (u16)Command << 8);	//low 8 bits of bus por is not used 
	GPIO_WriteBit(LM_CS_GPIO_Port, LM_CS_GPIO_Pin, Bit_SET);
	GPIO_WriteBit(LM_WR_GPIO_Port, LM_WR_GPIO_Pin, Bit_RESET);	
	GPIO_WriteBit(LM_WR_GPIO_Port, LM_WR_GPIO_Pin, Bit_SET);
	GPIO_WriteBit(LM_CS_GPIO_Port, LM_CS_GPIO_Pin, Bit_RESET);	
}
/*---------------------------------------------------------------------------
**	Function	: 
**	Parameters	:
**	Retval		:
**	Description	: 
---------------------------------------------------------------------------*/
void LM_SdData(u8 DData)   //send data
{
	GPIO_WriteBit(LM_DC_GPIO_Port, LM_DC_GPIO_Pin, Bit_SET);
	GPIO_Write(LM_DATA_BUS_Port, (u16)DData << 8);			//low 8 bits of bus por is not used 
	GPIO_WriteBit(LM_CS_GPIO_Port, LM_CS_GPIO_Pin, Bit_SET);
	GPIO_WriteBit(LM_WR_GPIO_Port, LM_WR_GPIO_Pin, Bit_RESET);	
	GPIO_WriteBit(LM_WR_GPIO_Port, LM_WR_GPIO_Pin, Bit_SET);
	GPIO_WriteBit(LM_CS_GPIO_Port, LM_CS_GPIO_Pin, Bit_RESET);	
}
/*---------------------------------------------------------------------------
**	Function	: 
**	Parameters	:
**	Retval		:
**	Description	: 
---------------------------------------------------------------------------*/
void LM_Init(void)
{
	GPIO_WriteBit(LM_RST_GPIO_Port, LM_RST_GPIO_Pin, Bit_SET);
	delay_ms(100);
	GPIO_WriteBit(LM_RST_GPIO_Port, LM_RST_GPIO_Pin, Bit_RESET);
	delay_ms(1);
	GPIO_WriteBit(LM_RST_GPIO_Port, LM_RST_GPIO_Pin, Bit_SET);
	delay_ms(50);
	
	LM_SdCmd(0x26);            	// 设置占空比1/128，温度系数设置0.1%/C
    LM_SdCmd(0x40);            	// 设置显示起始行 = 0
    LM_SdCmd(0x89);            	// 设置页/列地址自动加1
    LM_SdCmd(0xAF);            	// 开显示
    LM_SdCmd(0x90);            	// 固定区域行数 = 0
	LM_SdCmd(0xC4);            	// 设置水平映像为逆序，垂直映像为正序，数据排序D0-D7
    LM_SdCmd(0xEA);           	// 设置占空比1/12
    LM_SdCmd(0x2D);            	// 设置内部LCD电源
   	LM_SdCmd(0x81);            	// 设置对比度
	LM_SdCmd(0x20);   			// 对比度初始值
}
