void LM_DispChar(u16 x,u16 y,char Char)
{
	u16 i;
	u16 CharTabLocation;
	
	LM_SdCmd(y|0xb0);               // 设置页地址
	LM_SdCmd(x&0x0f);               // 设置列地址低4位
	LM_SdCmd((x>>4)|0x10);          // 设置列地址高4位
	
	CharTabLocation = (Char - 32)<<4;
	for(i=0;i<8;i++)
		LM_SdData(ASCIITAB8_16[CharTabLocation + i]);  	// 写字模数据
	
	LM_SdCmd((y+1)|0xb0);               // 设置页地址
	LM_SdCmd(x&0x0f);               // 设置列地址低4位
	LM_SdCmd((x>>4)|0x10);          // 设置列地址高4位
	
	for(i=8;i<16;i++)
		LM_SdData(ASCIITAB8_16[CharTabLocation+i]);  	// 写字模数据
	
}
